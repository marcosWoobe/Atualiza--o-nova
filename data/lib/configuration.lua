function getPortraitClientID(pokeName)
	local random_shinys = {
		["Shiny Crobat"] = 12487,
		
		["Shiny Magmar"] = 12027,
		["Electivire"] = 12910,
		["Magmortar"] = 12905,
		["Shiny Magmortar"] = 12935,
		["Shiny Electivire"] = 12932,


		["Shiny Ampharos"] = 12074,
		["Shiny Feraligatr"] = 12076,
		["Shiny Larvitar"] = 12078,
		["Shiny Machamp"] = 12080,
		["Shiny Meganium"] = 12082,
		["Shiny Pupitar"] = 12084,
		["Shiny Tauros"] = 12086,
		["Shiny Typhlosion"] = 12088,
		["Shiny Xatu"] = 12090,

		["Shiny Magcargo"] = 12790,
		["Shiny Lanturn"] = 12791,
		["Crystal Onix"] = 11427,
	}
	local orN = pokeName
	if isOutlandPoke(pokeName) then
		for i,n in pairs(outlandNames) do
			if pokeName:find(n) then
				pokeName = pokeName:gsub(n, "")
			end
		end
	end
	if isOutlandElder(pokeName) then
		for i,n in pairs(outlandElder) do
			if pokeName:find(n) then
				pokeName = pokeName:gsub(n, "")
				if pokes["Shiny ".. pokeName] then
					pokeName = "Shiny ".. pokeName
				end
			end
		end
	end
	local id = fotos[pokeName]
	if not id then
		print(orN .." não encontrado na função: getPortraitClientID.")
		return 3283
	end
	if id >= 11989 and id <= 12139 then -- kanto
	   return id - 928
	elseif id >= 11139 and id <= 11285 or id >= 11288 and id <= 11387 then -- shinys kanto e johto
	   return id - 911
	elseif id >= 12607 and id <= 12616 then -- alguns shinys johto
	   return id - 1178
	elseif id >= 14795 and id <= 14813 then
		return id - 997
	elseif id >= 14814 and id <= 14879 then
		return id - 996
	elseif id == 14880 then -- ninjask
		return 13817
	elseif random_shinys[pokeName] then
		return random_shinys[pokeName]
    end
	print("Pokemon nao encontrado: " .. pokeName .. ", na funcao: getPortraitClientID")
	return 3283
end


