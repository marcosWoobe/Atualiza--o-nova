function doAdjustPokeClan(pkmn, clan, rank)
	if not isCreature(pkmn) then return false end
	local myType = getPokemonType(pkmn)
	if not (isInArray(clanTypes[clan], myType.type1) or isInArray(clanTypes[clan], myType.type2)) then return false end
	
	if not getPlayerStorageValue(pkmn, 1001) then print("No storage 1001. (yet?)") return false end
	if not getPlayerStorageValue(pkmn, 1002) then print("No storage 1002. (yet?)") return false end
	if not getPlayerStorageValue(pkmn, 1003) then print("No storage 1003. (yet?)") return false end
	if not getPlayerStorageValue(pkmn, 1004) then print("No storage 1004. (yet?)") return false end
	if not getPlayerStorageValue(pkmn, 1005) then print("No storage 1005. (yet?)") return false end
	
	local bonus = 0.04*rank + 1 -- 4% por rank
	if getPlayerStorageValue(pkmn, 123713) ~=  1 then
		setPlayerStorageValue(pkmn, 123713, 1)
		for x=1001,1005 do
			setPlayerStorageValue(pkmn, x, getPlayerStorageValue(pkmn, x)*bonus)
		end
		--doSendMsg(getCreatureMaster(pkmn), "Your ".. clan .." pokémon got a ".. 4*rank .."% bonus stats.")
	end
	return true
end

function getClosestTarget(cid)
	if not isCreature(cid) then return false end
	--local god = getPlayerByName("[*] Uissu")
		--doSendMsg(god, "Attempting to check ".. getCreatureName(cid) .."'s closest target.")
	local rangeX, rangeY = 9, 9
	local spectators = getSpectators(getCreaturePosition(cid), rangeX, rangeY, false)
	local specPk = {}
	local specPl = {}

	for i,pid in ipairs(spectators) do
		if isSummon(pid) and pid ~= cid then
				--doSendMsg(god, "Table insert summon: "..pid.." at "..getDistanceBetween(getCreaturePosition(cid), getCreaturePosition(pid)).." distance")
			table.insert(specPk,{pid=pid,dist=getDistanceBetween(getCreaturePosition(cid), getCreaturePosition(pid))})
		elseif isPlayer(pid) and getPlayerGroupId(pid) < 4  and pid ~= cid then
			table.insert(specPl,{pid=pid,dist=getDistanceBetween(getCreaturePosition(cid), getCreaturePosition(pid))})
				--doSendMsg(god, "Table insert player: "..pid.." at "..getDistanceBetween(getCreaturePosition(cid), getCreaturePosition(pid)).." distance")
		end
	end
	local target = 0
	local lowestdist = 1000
	if #specPk > 0 then
			--doSendMsg(god, "Summon table count: ".. #specPk)
		for i,v in pairs(specPk) do
			if v.dist < lowestdist then
				target = v.pid
				lowestdist = v.dist
			end
		end
	elseif #specPl > 0 then
			--doSendMsg(god, "Player table count: ".. #specPl)
		for i,v in pairs(specPl) do
			if v.dist < lowestdist then
				target = v.pid
				lowestdist = v.dist
			end
		end
	else
		return false
	end
		--doSendMsg(god, "-> Target found: ".. getCreatureName(target))
	return target
end

